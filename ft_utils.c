#include "ft_printf.h"

int	ft_strchr(char c, char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] == c)
			return (c);
		i++;
	}
	if (str[i] == c)
		return (c);
	return (-1);
}
